func TestScanCatchesInterfaceAndAlias(t *testing.T) {
	root := t.TempDir()
	write(t, filepath.Join(root, "iface.go"),
		`package x
type WorkItem interface { Title() string }
`)
	write(t, filepath.Join(root, "alias.go"),
		`package x
type WorkItem = int
`)
	terms := []Term{{Pascal: "WorkItem"}}
	found, _ := Scan(root, terms, nil)
	if len(found) != 2 {
		t.Errorf("expected 2 findings (interface + alias), got %d (%+v)", len(found), found)
	}
}
