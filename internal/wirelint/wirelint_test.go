package wirelint

import (
	"os"
	"path/filepath"
	"testing"
)

func write(t *testing.T, path, content string) {
	t.Helper()
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		t.Fatal(err)
	}
}

// validQueryIR returns a minimal kind=query IR document with the given
// wire_name. Used to populate ir/domain/ for tests.
func validQueryIR(name, wire string) string {
	return `{
  "kind": "query",
  "name": "` + name + `",
  "wire_name": "` + wire + `",
  "returns": {"shape": "list", "type": "work-item"},
  "source": {
    "dsl_file": "dsl/domain/` + name + `.lisp",
    "sha256": "0123456789abcdef0123456789abcdef0123456789abcdef0123456789abcdef"
  }
}`
}

