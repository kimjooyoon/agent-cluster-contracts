func TestScanEmptyWiresNoOp(t *testing.T) {
	root := t.TempDir()
	write(t, filepath.Join(root, "anything.go"), `const x = "workItems"`)
	found, err := Scan(root, nil, nil)
	if err != nil {
		t.Fatal(err)
	}
	if len(found) != 0 {
		t.Errorf("expected 0 findings when wires is empty, got %+v", found)
	}
}

func TestScanMatchesBothQuoteStyles(t *testing.T) {
	root := t.TempDir()
	write(t, filepath.Join(root, "x.dart"),
		`var q = 'workItems';
var p = "workItems";
`)
	wires := []Wire{{Name: "workItems"}}
	found, _ := Scan(root, wires, nil)
	if len(found) != 2 {
		t.Errorf("expected 2 findings (both quote styles), got %d: %+v", len(found), found)
	}
}

func TestScanIgnoresIdentifierLikeOccurrence(t *testing.T) {
	root := t.TempDir()
	// Unquoted identifier — not a string literal, should NOT match.
	write(t, filepath.Join(root, "x.go"),
		`package x
const workItems = 1
`)
	wires := []Wire{{Name: "workItems"}}
	found, _ := Scan(root, wires, nil)
	if len(found) != 0 {
		t.Errorf("expected 0 findings (identifier, not string literal), got %+v", found)
	}
}
