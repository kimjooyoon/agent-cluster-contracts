func TestLoadWiresOnlyQueryKind(t *testing.T) {
	root := t.TempDir()
	irDir := filepath.Join(root, "domain")
	write(t, filepath.Join(irDir, "list-things.ir.json"), validQueryIR("list-things", "things"))
	// An aggregate doc — must be ignored.
	write(t, filepath.Join(irDir, "thing.ir.json"), `{
  "kind": "aggregate", "name": "thing",
  "slots": [{"name":"id","type":"string","required":true}],
  "source": {"dsl_file": "dsl/x.lisp", "sha256": "`+pad64("a")+`"}
}`)
	wires, err := LoadWires(irDir)
	if err != nil {
		t.Fatal(err)
	}
	if len(wires) != 1 {
		t.Fatalf("expected 1 wire, got %d (%v)", len(wires), wires)
	}
	if wires[0].Name != "things" {
		t.Errorf("wire = %q", wires[0].Name)
	}
}

func pad64(seed string) string {
	out := ""
	for len(out) < 64 {
		out += seed
	}
	return out[:64]
}

func TestScanFlagsLiteralInUnallowedFile(t *testing.T) {
	root := t.TempDir()
	write(t, filepath.Join(root, "internal", "slice", "server.go"),
		`package slice
const q = "workItems"
`)
	wires := []Wire{{Name: "workItems", IRPath: "ir/domain/list-work-items.ir.json", IRDocName: "list-work-items"}}
	found, err := Scan(root, wires, []string{"internal/contracts"})
	if err != nil {
		t.Fatal(err)
	}
	if len(found) != 1 {
		t.Fatalf("expected 1 finding, got %d: %+v", len(found), found)
	}
	if found[0].Wire != "workItems" || found[0].Line != 2 {
		t.Errorf("finding wrong: %+v", found[0])
	}
}

func TestScanSkipsGeneratedFile(t *testing.T) {
	root := t.TempDir()
	write(t, filepath.Join(root, "internal", "slice", "generated.go"),
		`// Code generated by gen-go-client. DO NOT EDIT.
//
// (intentionally outside the excluded dir to prove header detection works)
package slice
const q = "workItems"
`)
	wires := []Wire{{Name: "workItems"}}
	found, _ := Scan(root, wires, nil)
	if len(found) != 0 {
		t.Errorf("expected 0 findings (generated file should be skipped), got %d: %+v", len(found), found)
	}
}

